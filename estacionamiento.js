var caja = document.getElementById('caja'),
    contador=0;

    function cambio()
    { if(contador==0)
        {
            caja.classList.add('rojo');
            contador=1;
            alert("solo si heres discapacitado");
            
        }
        else{caja.classList.remove('rojo');
            contador=0;
            alert("lo quieres desocupar");
            
        }
    }
    caja.addEventListener('click',cambio,true)

